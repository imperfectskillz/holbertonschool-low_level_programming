#include "3-calc.h"

/**
 *op_add - adds a and b
 *@a: first int
 *@b: second int
 *Return: sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 *op_sub = subtracts a and b
 *@a: first int
 *@b: second int
 *Return: sub
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 *op_mul - multiplies a and b
 *@a: first int
 *@b: second int
 *Return: mult
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 *op_div - divides
 *@a: var
 *@b: var
 *Return: div
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 *op_mod - modulo
 *@a: var
 *@b: var
 *Return: mod
 */

int op_mod(int a, int b)
{
	return (a % b);
}
